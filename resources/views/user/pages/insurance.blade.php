CHÍNH SÁCH BẢO HÀNH


1. Mục đích

- Đảm bảo quyền lợi và lợi ích của khách hàng trong việc sử dụng các sản phẩm của TECHPRO,

- Đảm bảo hàng hóa của Công ty không bị suy giảm giá trị khi phát sinh sự cố phải thực hiện bảo hành.

- Bảo hành sản phẩm: Khắc phục những lỗi hỏng hóc, sự cố kỹ thuật xảy ra do lỗi của nhà sản xuất.
- Bảo trì, bảo dưỡng: Bao gồm lau chùi vệ sinh sản phẩm, kiểm tra hệ thống, sửa chữa những hỏng hóc nhỏ có thể sửa được (không bao gồm thay thế thiết bị). Thời gian và chi phí (nếu có) bảo trì, bảo dưỡng tùy thuộc vào sự thỏa thuận giữa TECHPRO và Quý khách hàng.


2. Phạm vi áp dụng
Tất cả các sản phẩm do TECHPRO cung cấp không vi phạm điều kiện và chính sách bảo hành của Hãng/NCC


3. Nội dung

Quý khách được bảo hành miễn phí các sản phẩm, thiết bị do TECHPRO cung cấp nếu đáp ứng đầy đủ các điều kiện bảo hành như sau:

Thời gian

Dưới 06 tháng

06 tháng – 01 năm

Trên 01 năm

Chi tiết nội dung Bảo hành

Quý khách sẽ được đổi ngay một sản phẩm mới khác.

Quý khách sẽ được đổi một sản phẩm, thiết bị khác tương đương.

Đối với sản phẩm, thiết bị tính từ thời điểm mua đến thời điểm yêu cầu bảo hành có thời hạn

Quý khách sẽ được mượn một sản phẩm, thiết bị khác dùng thay thế trong khi chờ TECHPRO làm thủ tục bảo hành với các nhà cung cấp.

Đối với những sản phẩm khách hàng có yêu cầu kéo dài thêm thời gian bảo hành hơn nữa so với thời gian bảo hành tiêu chuẩn Công ty TECHPRO sẽ thu phí theo thỏa thuận cho từng sản phẩm cụ thể.

Quy trình bảo hành

+ Bảo hành các thiết bị tin học là khắc phục những lỗi hỏng hóc, sự cố kỹ thuật xảy ra do lỗi của nhà cung cấp.

+ Trong quá trình vận hành, khi gặp sự cố yêu cầu Quý khách liên hệ với Bộ phận bảo hành của TECHPRO để được trợ giúp.

+ Nếu sự cố gặp phải là do thao tác không đúng kỹ thuật hay vận hành không đúng quy trình sẽ được kỹ thuật viên của TECHPRO hướng dẫn thao tác, vận hành lại.

+ Nếu sự cố gặp phải không khắc phục được ngay TECHPRO sẽ cử Kỹ thuật viên đến kiểm tra lại thiết bị, sản phẩm và khắc phục tại chỗ cho Quý khách (Dịch vụ khắc phục tại chỗ là không bắt buộc với Techpro, tuy nhiên Techpro sẽ thực hiện nếu có thể nhằm hỗ trợ khách hàng tốt nhất)

+ Nếu không thể khắc phục được ngay sự cố, TECHPRO sẽ nhận lại thiết bị, sản phẩm và giao cho Quý khách một sản phẩm khác theo các quy định tại Mục 1:Các ưu đãi bảo hành.

+ Trong trường hợp Kỹ thuật viên của TECHPRO không thể đến tận nơi được rất mong Quý khách trực tiếp mang thiết bị, sản phẩm đến công ty để được bảo hành



TECHPRO cam kết trả sản phẩm, thiết bị bảo hành cho Quý khách trong thời gian ngắn nhất (Với sản phẩm thông thường là 30 ngày, với sản phẩm chuyên dụng là 45 ngày)

Những trường hợp không được bảo hành
+ Sản phẩm, thiết bị đã hết hạn bảo hành: Đối với những sản phẩm thiết bị đã hết hạn bảo hành, Quý khách phải chịu mọi chi phí sửa chữa, thay thế thiết bị. Techpro sẽ thông báo cho khách hàng các chi phí liên quan trước khi thực hiện việc sửa chữa và khách hàng phải có xác nhận của người có thẩm quyền vào thông báo của Techpro.

+ Chi phí sửa chữa, thay thế sẽ theo bảng giá, biểu giá do TECHPRO ban hành.

+ TECHPRO không chịu trách nhiệm đối với những lỗi sau của sản phẩm:

Số seri trên thiết bị không xác định được hoặc sai so với Phiếu bảo hành.
Các phần mềm cung cấp miễn phí kèm theo thiết bị, sản phẩm.
Không có Phiếu bảo hành, hoặc Phiếu bảo hành bị rách rời, chắp vá, tẩy xoá;
Tem bảo hành không còn nguyên vẹn, bị rách, vỡ hoặc bị dán đè lên, ngày ghi trên tem bị sửa đổi.
Quý khách tự ý tháo dỡ, sửa chữa.
Thiết bị, sản phẩm bị hỏng do tác động của lực cơ học làm cho biến dạng, rơi vỡ; bị xước.
Thiết bị sản phẩm bị hỏng do ẩm ướt, hoen rỉ, chảy nước hoặc bị động vật xâm nhập vào.
Thiết bị, sản phẩm bị hỏng do thiên tai, hoả hoạn, cháy nổ, bao gồm cả cháy nổ do quạt bị hỏng gây ra. Các thiết bị, sản phẩm bị hỏng do Quý khách sử dụng sai quy định về điện áp hoặc do cố ý làm hỏng.
+ TECHPRO không bảo hành cho các sản phẩm, thiết bị: pin CMOS, các loại cáp điện, các tín hiệu, các bộ nắn dòng, công tắc nguồn, đèn tín hiệu, tai nghe theo thiết bị, quạt trên thiết bị.

+ Đối với một số trường hợp cá biệt như CPU cong hoặc gẫy chân; ổ cứng nứt, vỡ IDE hoặc bị cháy, nổ, móp méo, chữ trên lưng ổ bị mờ, bong tem bạc, mạch bị hoen rỉ, oxy hoá TECHPRO không chịu trách nhiệm bảo hành


